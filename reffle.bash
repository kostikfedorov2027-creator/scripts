#!/bin/bash

# --------------------------------------------------
#  Меню для Ubuntu с возможностью выбора действий
#  Чтобы добавить свой пункт:
#    1. Добавьте название в массив options
#    2. Добавьте новую ветку в case с нужными командами
# --------------------------------------------------

# Функция паузы (чтобы увидеть результат перед возвратом в меню)
pause() {
    echo
    read -p "Нажмите Enter, чтобы продолжить..."
}

# Основной цикл
while true; do
    clear
    echo "========================================="
    echo "         Управление системой Ubuntu        "
    echo "========================================="
    echo

    # Список пунктов меню
    options=(
        "Информация о системе"
        "Список активных служб"
        "Использование дисков"
        "Обновить систему"
        "Запустить свой скрипт (пример)"
        "Выход"
    )

    # Вывод меню с помощью select (автоматически нумерует)
    PS3="Выберите пункт (введите номер): "
    select choice in "${options[@]}"; do
        # Проверяем, что выбран существующий пункт
        if [[ -z "$choice" ]]; then
            echo "Неверный номер. Попробуйте снова."
            continue
        fi

        # Выполняем действие в зависимости от выбора
        case "$choice" in
            "Информация о системе")
                clear
                echo "--- ИНФОРМАЦИЯ О СИСТЕМЕ ---"
                lsb_release -ds
                uname -r
                uname -m
                uptime -p
                free -h
                pause
                break
                ;;

            "Список активных служб")
                clear
                echo "--- АКТИВНЫЕ СЛУЖБЫ (первые 20) ---"
                systemctl list-units --type=service --state=running --no-pager | head -n 20
                pause
                break
                ;;

            "Использование дисков")
                clear
                echo "--- ИСПОЛЬЗОВАНИЕ ДИСКОВ ---"
                df -h
                pause
                break
                ;;

            "Обновить систему")
                clear
                echo "--- ОБНОВЛЕНИЕ СИСТЕМЫ ---"
                read -p "Выполнить sudo apt update && sudo apt upgrade -y? (y/N): " confirm
                if [[ "$confirm" =~ ^[Yy]$ ]]; then
                    sudo apt update && sudo apt upgrade -y
                else
                    echo "Отменено."
                fi
                pause
                break
                ;;

            "Запустить свой скрипт (пример)")
                clear
                echo "--- ЗАПУСК ВНЕШНЕГО СКРИПТА ---"
                # Здесь можно указать путь к вашему скрипту, например:
                # ./мой_скрипт.sh
                # или встроить команды прямо сюда:
                echo "Привет! Это пример выполнения вашего собственного кода."
                echo "Вы можете заменить эту секцию на вызов другого скрипта."
                pause
                break
                ;;

            "Выход")
                echo "До свидания!"
                exit 0
                ;;

            *)
                echo "Неизвестный пункт. Попробуйте снова."
                break
                ;;
        esac
    done
done
